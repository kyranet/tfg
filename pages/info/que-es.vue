			El Aprendizaje-Servicio (<ApS /> de ahora en adelante) es una metodología de aprendizaje cuya presencia se hace cada vez más patente en la
			educación superior, y consiste en implicar al alumno en proyectos que cumplan una función social, realizando servicios de carácter cívico
