		</p>
		<p>
			Es precisamente la cualidad cívica de los proyectos lo que produce un valor difícilmente alcanzable en los contextos de formación
			tradicionales, siendo de esta forma el <ApS /> un aprendizaje de gran valor que se alcanza de forma transversal en el desarrollo formativo
