		<DefineApSPortal><a href="#que-es-portal-aps">PORTAL</a> <ApS /></DefineApSPortal>
		<h2>
			¿Qué es este <dfn id="que-es-portal-aps">Portal <ApS /></dfn> y para qué sirve?
		</h2>
		<p>
			Este proyecto es la tercera parte de desarrollo de una comunidad web para el soporte informático de Aprendizaje-Servicio. Esta aplicación
			sirve al propósito de organizar y centralizar recursos sobre Aprendizaje-Servicio, con el fin de promover y dar soporte a actividades
			educativas de Aprendizaje-Servicio en diversos contextos académicos.
		</p>
		<p>
			Es importante resaltar que el término Virtual hace referencia a la forma en la que organizan los recursos del proyecto, y no al proyecto
