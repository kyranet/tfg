		</p>

		<h2>¿Cómo se genera un proyecto <ApS />?</h2>
		<p>
			El portal aps es una aplicación web por medio de la cual se pone en contacto a todas las partes interesadas en desarrollar un proyecto de
			aprendizaje-servicio. estas partes son las siguientes:
		</p>
		<ul>
			<li>
				<strong>La ENTIDAD:</strong> Son aquellas instituciones, entidades, organismos, etc. que pueden identificar una necesidad en su seno o
				entorno, y desean PRESENTAR UNA OFERTA que puede ser RESPALDADA por un PROFESOR.
			</li>
			<li>
				<strong>El PROFESOR:</strong> Es un docente en alguna universidad interesado en RESPALDAR UNA OFERTA presentada por alguna ENTIDAD, o
				interesado en PRESENTAR UNA OFERTA él mismo que pueda ser de interés para alguna ENTIDAD. Se encarga de gestionar el
				<a href="#que-es-proyecto">PROYECTO</a> <ApS />, y de aceptar ALUMNOS para que desarrollen el mismo. Puede ser de la universidad o
				EXTERNO.
			</li>
			<li>
				<strong>El ALUMNO:</strong> Es un estudiante que desea PARTICIPAR EN UN <a href="#que-es-proyecto">PROYECTO</a> <ApS /> para
				realizarlo bajo la tutorización del PROFESOR. Puede ser de la universidad o EXTERNO.
			</li>
		</ul>

		<DefineApSOffer><a href="#que-es-oferta-aps">OFERTA</a></DefineApSOffer>
		<h2>¿Qué es una <dfn id="que-es-oferta-aps">Oferta</dfn>?</h2>
		<p>
			Un <a href="#que-es-proyecto">PROYECTO</a> <ApS /> siempre comienza a partir de un objeto germinal al que se denomina <ApSOffer />. Una
			<ApSOffer />, de hecho, podría dar lugar a más de un <a href="#que-es-proyecto">PROYECTO</a> <ApS />.
		</p>
		<p>
			Una <ApSOffer /> es una idea plasmada en varios párrafos sobre una necesidad social que puede ser cubierta a través de un
			<a href="#que-es-proyecto">PROYECTO</a> <ApS /> en el futuro. La <ApSOffer /> puede ser creada de las siguientes formas:
		</p>

		<ul>
			<li>La ENTIDAD plantea una necesidad social para que algún PROFESOR, en el futuro, pueda RESPALDARLA.</li>
			<li>
				El PROFESOR plantea un proyecto que cubra una posible necesidad social para que alguna ENTIDAD pueda, en el futuro, valorar si tiene
				dicha necesidad social.
			</li>
		</ul>

		<p>Existe, además, dos casos especiales en los que se puede crear una <ApSOffer />:</p>
		<ul>
			<li>
				Un ALUMNO puede PROPONER, a través de un formulario del <ApSPortal />, una <ApSOffer />. El GESTOR DEL <ApSPortal /> recibirá dicha
				PROPUESTA y decidirá si la RECHAZA o la redirige a la ENTIDAD o PROFESOR que crea convenientes para que ellos creen la
				<ApSOffer /> apropiada.
			</li>
			<li>
				El GESTOR DEL <ApSPortal /> puede RESPALDAR una <ApSOffer /> por dos razones:
				<ul>
					<li>La <ApSOffer /> es de tal magnitud que no puede ser RESPALDADA por un único PROFESOR.</li>
					<li>La <ApSOffer /> da lugar a más de un <a href="#que-es-proyecto">PROYECTO</a> <ApS />.</li>
				</ul>
			</li>
		</ul>

		<h2 id="que-es-partenariado">¿Qué es un PARTENARIADO?</h2>
		<p>
			En el <ApSPortal /> habrá un listado de OFERTAS planteadas, de modo que una ENTIDAD pueda entablar conversaciones con un PROFESOR y
			viceversa. A estas negociaciones sobre la <ApSOffer /> las llamamos PARTENARIADO. Por lo tanto, un PARTENARIADO puede tener diferentes
			estados según el progreso de la negociación de la <ApSOffer />:
		</p>
		<ul>
			<li>Estado EN NEGOCIACIÓN: La <ApSOffer /> está siendo discutida por un PROFESOR y una ENTIDAD.</li>
			<li>
				Estado ACORDADO: Ambas partes están de acuerdo en los términos, objetivos y procesos en los que una
				<ApSOffer /> va a convertirse en un <a href="#que-es-proyecto">PROYECTO</a> <ApS />. Es el nacimiento del
				<a href="#que-es-proyecto">PROYECTO</a> <ApS />.
			</li>
			<li>
				Estado SIN RESPUESTA: Una de las dos partes del PARTENARIADO no responde al otro, y por lo tanto el PARTENARIADO finaliza. La
				<ApSOffer />
				vuelve a estar disponible en el <ApSPortal /> para que sea discutida por otras personas.
			</li>
			<li>
				Estado SUSPENDIDO: Por cualquier razón, la <ApSOffer /> no puede progresar, y por lo tanto el PARTENARIADO finaliza. La
				<ApSOffer /> vuelva a estar disponible en el <ApSPortal /> para que sea discutida por otras personas.
			</li>
		</ul>
		<p>
			Así pues, una <ApSOffer /> (propuesta) da lugar a un PARTENARIADO (discusión) que a su vez se convierte en un
			<a href="#que-es-proyecto">PROYECTO</a> <ApS />.
		</p>

		<h2 id="que-es-proyecto">¿Qué es un PROYECTO <ApS />?</h2>
		<p>
			Como explicábamos en el apartado de partenariados, una <ApSOffer /> (propuesta) da lugar a un
			<a href="#que-es-partenariado">PARTENARIADO</a>
			(discusión) que a su vez se convierte en un PROYECTO <ApS />.
		</p>
		<p>
			Un PROYECTO <ApS /> al comenzar solo tiene dos de sus tres partes: ENTIDAD y PROFESOR. Ambas partes pueden ir aportando datos al PROYECTO
			<ApS /> mientras algún ALUMNO interesado SOLICITA PARTICIPAR en el PROYECTO <ApS />. Una vez haya alguna SOLICITUD DE ALUMNO, será el
			PROFESOR el encargado de ACEPTAR LA SOLICITUD.
		</p>
		<p>
			Cuando dicha SOLICITUD esté aceptada, entonces el PROYECTO <ApS /> tendrá sus tres partes fundamentales: ENTIDAD, PROFESOR y ALUMNO que
			INTEGRAN un PROYECTO <ApS />.
		</p>
		<p>
			Dentro del <ApSPortal /> existirá una PÁGINA para cada PROYECTO <ApS /> en la que los INTEGRANTES del PROYECTO <ApS /> pueden documentar
			el avance del proyecto, y tendrán HERRAMIENTAS COLABORATIVAS a su disposición para favorecer la comunicación entre los INTEGRANTES de
			este.
		</p>
		<p>Un PROYECTO <ApS /> presenta, por tanto, los siguientes ESTADOS:</p>
		<ul>
			<li>
				Estado ABIERTO: La <ApSOffer /> dio lugar a un <a href="#que-es-partenariado">PARTENARIADO</a> que llegó al estado ACORDADO, y por lo
				tanto se ha abierto un PROYECTO <ApS />, que está a la espera de recibir SOLICITUDES DE ALUMNOS. En este estado, PROFESOR y ENTIDAD
				pueden ir trabajando en el PROYECTO <ApS />. Es el PROFESOR el encargado de ACEPTAR LAS SOLICITUDES de los ALUMNOS.
			</li>
			<li>Estado EN CURSO: El PROYECTO <ApS /> ya no acepta más ALUMNOS. Seguirá en este estado mientras dure el desarrollo.</li>
			<li>
				Estado FINALIZADO: El PROYECTO <ApS /> ha terminado, con una conclusión satisfactoria. Este estado lo decide el PROFESOR desde el
				estado EN CURSO. Quedará archivado para futuras consultas públicas.
			</li>
			<li>
				Estado CANCELADO: El PROYECTO <ApS /> ha terminado, con una conclusión negativa. Este estado lo decide el PROFESOR desde los estados
				ABIERTO o EN CURSO. Quedará archivado para futuras consultas del GESTOR DEL <ApSPortal />.
			</li>
		</ul>
